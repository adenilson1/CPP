#include <iostream>
using namespace std;


int main() {
    //insertion operation

    cout << "Hi!\n";
    cout << 3.14 << endl;

    int x = 3;
    cout << x << endl;

    cout << "Enter your name: ";

    //extraction operation
    string name;
    cin >> name;

    //insertion operation
    cout << "Your name is: " << name;


    return 0;
}
